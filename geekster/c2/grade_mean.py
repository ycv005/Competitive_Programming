def gradeMean(grade):
    if grade == "A":
        return "Excellent"
    elif grade == "B":
        return "Good"
    elif grade == "C":
        return "Average"
    elif grade == "D":
        return "Deficient"
    elif grade == "F":
        return "Failing"
    else:
        return "Enter a Correct grade between A to F"
